    public Connection ConnectionDB() {
        Connection conector = null;
        String DBurl ="";
        try {
            if (tipoBase.getSelectedItem().toString().toLowerCase().contains("postgresql")) {
                DBurl="jdbc:postgresql://"+ipDb.getText()+":5432/" + dbName.getText();
                Class.forName("org.postgresql.Driver");               
            } else {
                Class.forName("com.mysql.jdbc.Driver");
            }
            conector = DriverManager.getConnection(DBurl,userDb.getText(),passDb.getText());
        } catch (ClassNotFoundException classNotFoundException) {
            JOptionPane.showMessageDialog(null, "Error con el driver JDBC");
        } catch (SQLException exception) {
            try {
                JOptionPane.showMessageDialog(null, "Error en la conexion","",JOptionPane.ERROR_MESSAGE);
                conector.close();
            } catch (SQLException ex) {
                
            }
        }
        return conector;
    }
    
    
 

