    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FileOutputStream fileeeee = null;
        try {
            JFileChooser guardar = new JFileChooser();
            guardar.setApproveButtonText("Guardar");
            guardar.showSaveDialog(null);
            String ruta = guardar.getSelectedFile().getPath() + ".pdf";
            File file = new File(ruta() + filee.getName());
            byte[] fileBytes = FileUtils.readFileToByteArray(file);
            FileOutputStream fileSalida = new FileOutputStream(ruta);
            fileSalida.write(fileBytes);
            fileSalida.close();
            JOptionPane.showMessageDialog(null, "Archivo guardado con exito");
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Error, No se puedo guardar el archivo", "", JOptionPane.ERROR_MESSAGE);
        } catch (IOException ex) {
            Logger.getLogger(Visual.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

